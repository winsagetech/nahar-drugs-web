		<script>
		var inArea,
            map = $('#shape1'),
            captions = {
                bangalore: ["Bangalore",
                    "images/places/bangalore.png"],
                mysore: ["Mysore",
                  "images/places/mysore.png"],
                tumkur: ["Tumkur",
                  "images/places/tumkur.png"],
                kolar: ["Kolar",
                 "images/places/kolar.png"],
				mandya: ["Mandya",
                 "images/places/mandya.png"],
				chitradurga: ["Chitradurga",
                 "images/places/chitradurga.png"]
            },
            single_opts = {
            	fill: true,
                fillColor: '000000',
                fillOpacity: 0,
                stroke: true,
                strokeColor: 'ff0000',
                strokeWidth: 5
            },
            all_opts = {
                fillColor: 'ffffff',
                fillOpacity: 0.6,
                stroke: true,
                strokeWidth: 5,
                strokeColor: 'ffffff'
            },
            initial_opts = {
                mapKey: 'data-name',
                isSelectable: false,
                clickNavigate : true,
                onMouseover: function (data) {
                    inArea = true;
                    $('#map-caption-header').text(captions[data.key][0]);                    
                    $('#places').attr("src",captions[data.key][1]);
                    $('#map-caption').show();
                },
                onMouseout: function (data) {
                    inArea = false; 
                    $('#map-caption-header').text("Karnataka");                    
                    $('#places').attr("src","images/places/karnataka.png");
                    $('#map-caption').show();                   
                }
            };
        opts = $.extend({}, all_opts, initial_opts, single_opts);


        // Bind to the image 'mouseover' and 'mouseout' events to activate or deactivate ALL the areas, like the
        // original demo. Check whether an area has been activated with "inArea" - IE<9 fires "onmouseover" 
        // again for the image when entering an area, so all areas would stay highlighted when entering
        // a specific area in those browsers otherwise. It makes no difference for other browsers.

        map.mapster('unbind')
            .mapster(opts)
            .bind('mouseover', function () {
                if (!inArea) {
                    map.mapster('set_options', all_opts)
                       .mapster('set', true, 'all')
                       .mapster('set_options', single_opts);
                }
            }).bind('mouseout', function () {
                if (!inArea) {
                    map.mapster('set', false, 'all');
                }
            });
			
	</script>
